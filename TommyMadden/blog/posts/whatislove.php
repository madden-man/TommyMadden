Just a super quick way of seeing on paper some of the emotions we feel every day but often forget to name specifically.

As you can see, many of them really are opposites - it's difficult to be both happy and sad, or both enraged and apathetic about something. On the other hand, some of them feed the other - insecurity feeds cockiness, just as often people act confident to avoid their fears.

But where does love fit into this? It could be something like this:
