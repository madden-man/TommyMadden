Because love is a choice, deep down - it's not just about that wild feeling of 'being in love'. And it does make people happy, it brings people together, it gives life...But there's more to it. It goes deeper than that somehow.

Why else would soldiers be willing to go off to fight wars to protect people they've never met? Why would mothers and fathers work two, three jobs just to support their children and give them a home? I mean, if it was only a choice - a purely human experience - or just a cognitive phemonon, how would we have the strength to do something like that?

So here I go trying to explain the unexplainable again. What a marvelous job.

Personally I don't think love can be described as a conventional 'emotion'. In my experience, it has always been more alive than anything, actively moving and interacting with the things around it. However, looking at emotions can help us learn a bit more about humanity and how we experience love.

