Because hate is the opposite of love, right?

Well, some believe so. If love is viewed as an emotion, then it is considered the strongest positive emotion, just as hate would be the strongest negative emotion. But like I said before, I don't think calling love an emotion really does it justice.

So I want to make an alternate proposal. I think each pair of emotions also has a spiritual component, which is related to the emotions but not reliant on them in any way. For example:
<div>
    <div align="left" style="float: left; margin-left: 5%; width: 30%;">
