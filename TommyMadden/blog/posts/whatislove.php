<p class="post_title">What is Love?</p>Baby don't hurt me.

I've been asking people this question over the last few days. First I'll ask, "Do you think someone can fall in love at first sight?" and the person inevitably says no in their roundabout way. And then I ask the real clincher: "What do you think love is?"

I've gotten all kinds of responses. Some say love is a feeling you get deep down when you want to be with someone. Some say it is a choice, a commitment to stay with someone forever. Others say that love is a feeling, a chemical imbalance in the brain that incentivizes attachment and ultimately reproduction.

But when they've each asked me what I think, I haven't had an answer.

