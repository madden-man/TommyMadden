Now how does this play into our whole discussion on love? Well let me tell you.

Like I said before, love is unexplainable. It's a conviction deep down in someone's soul that can't be chained down, can't be trampled on, and can't be ignored. It's uncontrollable, and if we're honest with ourselves, it has a mind of its own.

When I look at those characteristics in the middle column - 'courageous,' 'peaceful,' 'humble,' etc. - I see a human experience fully and absolutely uncontained by the world. I see someone untainted by their past, uncorrupted and blameless. As a Christian, I see Jesus. And it's a beautiful depiction of what a human can look like.

If we were to look at love as a bathrobe you can choose to put on or take off, it's like all these characteristics are different threads that weave together to form the truest form of love - the perfection that every human being desires deep down.

So what is love? Well, it is a choice, in that you have to seek it actively and wholeheartedly if you want to find it. But the reason we can experience love as humans is because it comes to seek us. We can't 'achieve' love no matter how hard we try, but when we take a step towards it, it in turn takes a step towards us, and leads us just a little further into the profound mystery that is life on this crazy Earth.

<img src="http://tommymadden.com/blog/img/1corinthians13-love.jpg" class="horizontal" />

If I had to give love an official name, I would call it God. But God is more than an old man in the heavens making decisions about what college we go to and how much money we make at our jobs. God is the force that binds us all together. The breath in our lungs, the wind blowing through the trees, the post it note your roommate leaves to make you smile...These things are my evidence of love.

To me, that is enough proof that God is alive, that love is powerful, and that I am involved in that mystery.

