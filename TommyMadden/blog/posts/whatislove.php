Whereas many will say that joy is just a synonym for happiness, I think it is something else entirely. Happiness is the condition of having fortunate circumstances - being comfortable where you are, and feeling like everything is going your way. But even if your life goes absolutely sour, if you lose everything you know and love and have to fight just to get up in the morning - even then I'd like to argue that you can have joy.

Because joy is a choice - you have to want it - and even though you have no control over it, when you <i>really</i> need it, it will be there. It rises up within you like a wave, whether you expect it to or not. If you've felt it the way I have, you know what I mean.

<img src="http://tommymadden.com/blog/img/love_mountain.jpg" class="horizontal" />

So that's the argument I'm making: Emotions are not one dimensional, one or the other, but multidimensional - One can be both sad and joyful because that person's spirit has chosen to find strength, and to not give up, and in response, joy or rest or humility came to seek that person.

